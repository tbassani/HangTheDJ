class Mix {
  constructor(id, title, ownerId, tracks, topTracks, currTrack, timeInterval) {
    this.id = id;
    this.title = title;
    this.ownerId = ownerId;
    this.tracks = tracks;
    this.topTracks = topTracks;
    this.currTrack = currTrack;
    this.timeInterval = timeInterval;
  }
}

export default Mix;
